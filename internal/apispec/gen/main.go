//go:build ignore

package main

import (
	"bytes"
	"errors"
	"fmt"
	"go/format"
	"io/fs"
	"os"
	"sort"
	"strings"

	"gopkg.in/yaml.v3"
)

const (
	specPath = "spec/opensearch-openapi.yaml"
	outPath  = "paths_gen.go"
)

var verbs = map[string]bool{
	"get": true, "put": true, "post": true, "delete": true,
	"head": true, "patch": true, "options": true, "trace": true,
}

type spec struct {
	Paths map[string]map[string]yaml.Node `yaml:"paths"`
}

func main() {
	if err := run(); err != nil {
		fmt.Fprintln(os.Stderr, "apispec gen:", err)
		os.Exit(1)
	}
}

func run() error {
	raw, err := os.ReadFile(specPath)
	if errors.Is(err, fs.ErrNotExist) {
		return fmt.Errorf("spec not found at %s; run `make update-spec` to fetch it", specPath)
	}
	if err != nil {
		return fmt.Errorf("reading spec: %w", err)
	}

	var s spec
	if err := yaml.Unmarshal(raw, &s); err != nil {
		return fmt.Errorf("parsing spec: %w", err)
	}

	templates := make([]string, 0, len(s.Paths))
	for tmpl := range s.Paths {
		templates = append(templates, tmpl)
	}
	sort.Strings(templates)

	var buf bytes.Buffer
	buf.WriteString("// Code generated by internal/apispec/gen. DO NOT EDIT.\n\n")
	buf.WriteString("package apispec\n\n")
	buf.WriteString("var Paths = []Endpoint{\n")
	for _, tmpl := range templates {
		methods := make([]string, 0, len(s.Paths[tmpl]))
		for key := range s.Paths[tmpl] {
			if verbs[strings.ToLower(key)] {
				methods = append(methods, strings.ToUpper(key))
			}
		}
		if len(methods) == 0 {
			continue
		}
		sort.Strings(methods)
		fmt.Fprintf(&buf, "\t{Template: %q, Methods: []string{%s}},\n", tmpl, quoteList(methods))
	}
	buf.WriteString("}\n")

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return fmt.Errorf("formatting output: %w", err)
	}

	if err := os.WriteFile(outPath, formatted, 0o644); err != nil {
		return fmt.Errorf("writing output: %w", err)
	}
	return nil
}

func quoteList(items []string) string {
	quoted := make([]string, len(items))
	for i, it := range items {
		quoted[i] = fmt.Sprintf("%q", it)
	}
	return strings.Join(quoted, ", ")
}
