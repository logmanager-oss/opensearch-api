package main

import (
	"bytes"
	"errors"
	"fmt"
	"go/format"
	"io/fs"
	"os"
	"sort"
	"strings"

	"github.com/pb33f/libopenapi"
	"github.com/pb33f/libopenapi/datamodel"
	"github.com/pb33f/libopenapi/datamodel/high/base"
	v3 "github.com/pb33f/libopenapi/datamodel/high/v3"
	"github.com/pb33f/libopenapi/orderedmap"
)

// Paths are relative to the gen module directory, which is the cwd when this
// generator runs (`go -C gen run .`); the outputs live one level up in apispec.
const (
	specPath    = "../spec/opensearch-openapi.yaml"
	outPath     = "../paths_gen.go"
	bodyOutPath = "../body_gen.go"
)

func main() {
	if err := run(); err != nil {
		fmt.Fprintln(os.Stderr, "apispec gen:", err)
		os.Exit(1)
	}
}

func run() error {
	raw, err := os.ReadFile(specPath)
	if errors.Is(err, fs.ErrNotExist) {
		return fmt.Errorf("spec not found at %s; run `make update-spec` to fetch it", specPath)
	}
	if err != nil {
		return fmt.Errorf("reading spec: %w", err)
	}

	// The OpenSearch spec has genuine circular schema references (e.g. search
	// Hit -> Explanation -> Hit); we only read top-level requestBody properties,
	// so skip the circular-reference check that would otherwise fail the build.
	doc, err := libopenapi.NewDocumentWithConfiguration(raw, &datamodel.DocumentConfiguration{
		SkipCircularReferenceCheck: true,
	})
	if err != nil {
		return fmt.Errorf("parsing spec: %w", err)
	}
	model, err := doc.BuildV3Model()
	if err != nil {
		return fmt.Errorf("building model: %w", err)
	}

	if err := writePaths(model.Model.Paths); err != nil {
		return fmt.Errorf("writing paths: %w", err)
	}
	if err := writeBodies(model.Model.Paths); err != nil {
		return fmt.Errorf("writing bodies: %w", err)
	}
	return nil
}

// writePaths emits paths_gen.go: every path template and its uppercased,
// sorted set of HTTP methods, in template order. Paths with no operations are
// skipped.
func writePaths(paths *v3.Paths) error {
	type endpoint struct {
		template string
		methods  []string
	}

	endpoints := make([]endpoint, 0, paths.PathItems.Len())
	for tmpl, item := range paths.PathItems.FromOldest() {
		ops := item.GetOperations()
		if ops == nil || ops.Len() == 0 {
			continue
		}
		methods := make([]string, 0, ops.Len())
		for method := range ops.FromOldest() {
			methods = append(methods, strings.ToUpper(method))
		}
		sort.Strings(methods)
		endpoints = append(endpoints, endpoint{template: tmpl, methods: methods})
	}
	sort.Slice(endpoints, func(i, j int) bool {
		return endpoints[i].template < endpoints[j].template
	})

	var buf bytes.Buffer
	buf.WriteString("// Code generated by internal/apispec/gen. DO NOT EDIT.\n\n")
	buf.WriteString("package apispec\n\n")
	buf.WriteString("var Paths = []Endpoint{\n")
	for _, ep := range endpoints {
		fmt.Fprintf(&buf, "\t{Template: %q, Methods: []string{%s}},\n", ep.template, quoteList(ep.methods))
	}
	buf.WriteString("}\n")

	return formatAndWrite(outPath, buf.Bytes())
}

type bodyField struct {
	Name string
	Type string
}

// writeBodies emits body_gen.go: the top-level field scaffold for every
// operation whose requestBody is a JSON object with named properties. Array and
// free-form bodies have no named top-level fields, so they are skipped.
func writeBodies(paths *v3.Paths) error {
	bodies := make(map[string][]bodyField)
	for tmpl, item := range paths.PathItems.FromOldest() {
		for method, op := range item.GetOperations().FromOldest() {
			fields := bodyFields(op)
			if len(fields) == 0 {
				continue
			}
			bodies[strings.ToUpper(method)+" "+tmpl] = fields
		}
	}

	keys := make([]string, 0, len(bodies))
	for k := range bodies {
		keys = append(keys, k)
	}
	sort.Strings(keys)

	var buf bytes.Buffer
	buf.WriteString("// Code generated by internal/apispec/gen. DO NOT EDIT.\n\n")
	buf.WriteString("package apispec\n\n")
	buf.WriteString("var Bodies = map[string][]BodyField{\n")
	for _, k := range keys {
		fmt.Fprintf(&buf, "\t%q: {", k)
		for _, f := range bodies[k] {
			fmt.Fprintf(&buf, "{Name: %q, Type: %q}, ", f.Name, f.Type)
		}
		buf.WriteString("},\n")
	}
	buf.WriteString("}\n")

	return formatAndWrite(bodyOutPath, buf.Bytes())
}

// bodyFields resolves an operation's requestBody to a JSON schema and returns
// its top-level fields in spec declaration order. It returns nil when the
// operation has no requestBody or the body is an array/free-form object.
// libopenapi resolves the requestBody $ref for us in the high model.
func bodyFields(op *v3.Operation) []bodyField {
	if op.RequestBody == nil || op.RequestBody.Content == nil {
		return nil
	}
	mt := pickJSON(op.RequestBody.Content)
	if mt == nil || mt.Schema == nil {
		return nil
	}
	schema := mt.Schema.Schema()
	if schema == nil || schema.Properties == nil {
		return nil
	}
	fields := make([]bodyField, 0, schema.Properties.Len())
	for name, proxy := range schema.Properties.FromOldest() {
		fields = append(fields, bodyField{Name: name, Type: scalarType(proxy.Schema())})
	}
	return fields
}

// scalarType returns the schema's scalar `type` when it is one of the known
// JSON types, else "" (composed schemas or a type union have no single type).
func scalarType(s *base.Schema) string {
	if s == nil || len(s.Type) != 1 {
		return ""
	}
	switch s.Type[0] {
	case "string", "integer", "number", "boolean", "object", "array":
		return s.Type[0]
	}
	return ""
}

// pickJSON selects the content entry whose media type contains "json"
// (covering application/x-ndjson too), falling back to the first entry.
func pickJSON(content *orderedmap.Map[string, *v3.MediaType]) *v3.MediaType {
	var first *v3.MediaType
	for media, mt := range content.FromOldest() {
		if strings.Contains(media, "json") {
			return mt
		}
		if first == nil {
			first = mt
		}
	}
	return first
}

func formatAndWrite(path string, src []byte) error {
	formatted, err := format.Source(src)
	if err != nil {
		return fmt.Errorf("formatting output: %w", err)
	}
	if err := os.WriteFile(path, formatted, 0o644); err != nil {
		return fmt.Errorf("writing output: %w", err)
	}
	return nil
}

func quoteList(items []string) string {
	quoted := make([]string, len(items))
	for i, it := range items {
		quoted[i] = fmt.Sprintf("%q", it)
	}
	return strings.Join(quoted, ", ")
}
