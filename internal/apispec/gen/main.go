package main

import (
	"bytes"
	"errors"
	"fmt"
	"go/format"
	"io/fs"
	"os"
	"sort"
	"strings"

	"github.com/pb33f/libopenapi"
	"github.com/pb33f/libopenapi/datamodel"
	v3 "github.com/pb33f/libopenapi/datamodel/high/v3"
)

// Paths are relative to the gen module directory, which is the cwd when this
// generator runs (`go -C gen run .`); the output lives one level up in apispec.
const (
	specPath = "../spec/opensearch-openapi.yaml"
	outPath  = "../paths_gen.go"
)

func main() {
	if err := run(); err != nil {
		fmt.Fprintln(os.Stderr, "apispec gen:", err)
		os.Exit(1)
	}
}

func run() error {
	raw, err := os.ReadFile(specPath)
	if errors.Is(err, fs.ErrNotExist) {
		return fmt.Errorf("spec not found at %s; run `make update-spec` to fetch it", specPath)
	}
	if err != nil {
		return fmt.Errorf("reading spec: %w", err)
	}

	// The OpenSearch spec has genuine circular schema references (e.g. search
	// Hit -> Explanation -> Hit); skip the circular-reference check that would
	// otherwise fail the build. We only read path templates and methods here.
	doc, err := libopenapi.NewDocumentWithConfiguration(raw, &datamodel.DocumentConfiguration{
		SkipCircularReferenceCheck: true,
	})
	if err != nil {
		return fmt.Errorf("parsing spec: %w", err)
	}
	model, err := doc.BuildV3Model()
	if err != nil {
		return fmt.Errorf("building model: %w", err)
	}
	return writePaths(model.Model.Paths)
}

// writePaths emits paths_gen.go: every path template and its uppercased, sorted
// set of HTTP methods, in template order. Paths with no operations are skipped.
func writePaths(paths *v3.Paths) error {
	type endpoint struct {
		template string
		methods  []string
	}

	endpoints := make([]endpoint, 0, paths.PathItems.Len())
	for tmpl, item := range paths.PathItems.FromOldest() {
		ops := item.GetOperations()
		if ops == nil || ops.Len() == 0 {
			continue
		}
		methods := make([]string, 0, ops.Len())
		for method := range ops.FromOldest() {
			methods = append(methods, strings.ToUpper(method))
		}
		sort.Strings(methods)
		endpoints = append(endpoints, endpoint{template: tmpl, methods: methods})
	}
	sort.Slice(endpoints, func(i, j int) bool {
		return endpoints[i].template < endpoints[j].template
	})

	var buf bytes.Buffer
	buf.WriteString("// Code generated by internal/apispec/gen. DO NOT EDIT.\n\n")
	buf.WriteString("package apispec\n\n")
	buf.WriteString("var Paths = []Endpoint{\n")
	for _, ep := range endpoints {
		fmt.Fprintf(&buf, "\t{Template: %q, Methods: []string{%s}},\n", ep.template, quoteList(ep.methods))
	}
	buf.WriteString("}\n")

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return fmt.Errorf("formatting output: %w", err)
	}
	if err := os.WriteFile(outPath, formatted, 0o644); err != nil {
		return fmt.Errorf("writing output: %w", err)
	}
	return nil
}

func quoteList(items []string) string {
	quoted := make([]string, len(items))
	for i, it := range items {
		quoted[i] = fmt.Sprintf("%q", it)
	}
	return strings.Join(quoted, ", ")
}
